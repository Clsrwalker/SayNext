const classroomStyleNotes =
  "classroom Q&A, short and direct, normal student tone, simple spoken English, slightly uncertain when natural";

const classroomAvoid = [
  "long setup",
  "essay-style answer",
  "AI lecture tone",
  "professor tone",
  "overly perfect grammar",
  "pretending to know when Xiang does not know",
];

function classroomSample(
  id: string,
  scene: string,
  transcript: string,
  idealAnswer: string,
  tags: string[] = [],
  styleNotes = classroomStyleNotes,
  avoid = classroomAvoid,
  expandedAnswer?: string,
): PersonalSample {
  return sample(
    id,
    scene,
    transcript,
    idealAnswer,
    ["classroom", "qna", ...tags],
    5,
    idealAnswer.length < 90 ? 5 : 4,
    expandedAnswer ? `${styleNotes}; expand if asked: ${expandedAnswer}` : styleNotes,
    avoid,
  );
}

